package com.sk.strngtst.test.demo2;

/**
 * @author shishir_kumar
 *
 */
public class Passenger {

	private int age;

	/**
	 * Instantiates a new passenger.
	 *
	 * @param age the age
	 */
	public Passenger(int age) {
		this.age = age;
	}

	/**
	 * Gets the age.
	 *
	 * @return the age
	 */
	public int getAge() {
		return age;
	}

	/**
	 * Sets the age.
	 *
	 * @param age the new age
	 */
	public void setAge(int age) {
		this.age = age;
	}

}
